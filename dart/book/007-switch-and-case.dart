void main(){
  // Switch Case Statements: Applicable for only 'int' and 'String'

  String grade = 'B';

  switch (grade) {
    case 'A':
    print("Execelent grade of A");
    break;

    case 'B':
    print("Very good");
    break;

    case 'C':
    print("Good enough, but work hard");
    break;

    case 'F':
    print("You have failed");
    break;

    default:
    print("Invalid Grade");
  }

}
